const exactFrench = Object.fromEntries(Object.entries(exactEnglish).map(([fr, en]) => [en, fr]));
const textNodes = new WeakMap();
const attrNames = ["placeholder", "aria-label", "title", "alt"];

const preserveSpace = (original, translated) => {
  const start = original.match(/^\s*/)?.[0] || "";
  const end = original.match(/\s*$/)?.[0] || "";
  return `${start}${translated}${end}`;
};

export const translateLiteral = (value, language) => {
  if (value === null || value === undefined) return value;
  const text = String(value);
  const trimmed = text.trim();
  if (!trimmed) return value;

  const dictionary = language === "en" ? exactEnglish : exactFrench;
  const translated = dictionary[trimmed];
  if (translated) return preserveSpace(text, translated);

  if (language === "en") {
    const backendMatch = trimmed.match(/^(.*) \((backend|backend indisponible)\)$/);
    if (backendMatch && exactEnglish[backendMatch[1]]) {
      return preserveSpace(text, `${exactEnglish[backendMatch[1]]} (${exactEnglish[backendMatch[2]] || backendMatch[2]})`);
    }
  }

  return value;
};

const shouldSkip = (node) => {
  const parent = node.parentElement;
  if (!parent) return true;
  return ["SCRIPT", "STYLE", "TEXTAREA", "INPUT", "OPTION"].includes(parent.tagName);
};

const translateTextNode = (node, language) => {
  if (shouldSkip(node)) return;
  if (!textNodes.has(node)) textNodes.set(node, node.nodeValue);
  const original = textNodes.get(node);
  const next = translateLiteral(original, language);
  if (node.nodeValue !== next) node.nodeValue = next;
};

const translateElementAttributes = (element, language) => {
  attrNames.forEach((attr) => {
    if (!element.hasAttribute?.(attr)) return;
    const storeAttr = `data-i18n-original-${attr}`;
    if (!element.hasAttribute(storeAttr)) {
      element.setAttribute(storeAttr, element.getAttribute(attr));
    }
    const original = element.getAttribute(storeAttr);
    const next = translateLiteral(original, language);
    if (element.getAttribute(attr) !== next) element.setAttribute(attr, next);
  });
};

export const applyDomTranslations = (root, language) => {
  if (!root) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  let node = walker.nextNode();
  while (node) {
    translateTextNode(node, language);
    node = walker.nextNode();
  }
  root.querySelectorAll?.("*").forEach((element) => translateElementAttributes(element, language));
};
